package corebasic;

public class SwitchCase {

	public static void main(String[] args) {
		
		byte b = 23;
		switch (b) {
		case 21: 
				
				System.out.println("21 is qual to b");
		break;
		case 22:
				System.out.println("22 is Not Equal to");
		break;
		case 23:
				System.out.println("23 is equal to");
		break;
		default:
			 System.out.println("number is Not Matching");
		}
	}
}
